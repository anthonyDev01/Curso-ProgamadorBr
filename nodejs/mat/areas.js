module.exports.quadrado = quadrado = (lado) => {return lado * lado};
module.exports.retangulo = retangulo = (base, lado) => {return base * lado};
module.exports.triangulo = triangulo = (base, lado) => {return base * lado};
module.exports.circulo = circulo = (raio) => { const pi = 3.1416; return pi * raio * raio};